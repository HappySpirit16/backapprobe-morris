package co.approbe.simulator.loan.lambda;

public class Loan {
	
	
	private int fees;
	private int value;
	public int getFees() {
		return fees;
	}
	public void setFees(int fees) {
		this.fees = fees;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	

}
